uint64_t traceport_timestamp(void) {
  uint32_t msb_a = htim5.Instance->CNT;
  uint32_t lsb_a = htim2.Instance->CNT;
  uint32_t msb_b = htim5.Instance->CNT;

  if (msb_a == msb_b) {
    uint64_t result = lsb_a | (((uint64_t)msb_a) << 32);
    return result;
  } else {
    uint32_t lsb_b = htim2.Instance->CNT;
    uint64_t result = lsb_b | (((uint64_t)msb_b) << 32);
    return result;
  }
}

